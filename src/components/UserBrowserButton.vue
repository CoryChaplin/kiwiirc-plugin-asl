<template id="user-browser">
    <div :class="[active ? 'kiwi-header-option--active': '']" class="kiwi-header-option">
        <a @click.prevent="toggleUserBrowser">
            <i :class="icon" class="fa" aria-hidden="true"/>
            <span class="kiwi-header-user-browser-span">Chatteurs</span>
            <!-- {{ $t('plugin-asl:user_button_label') }} -->
        </a>
    </div>
</template>

<script>

/* global kiwi:true */

import UserBrowser from './UserBrowser.vue';

export default {
    data() {
        return {
            active: false,
        };
    },
    computed: {
        icon() {
            return kiwi.state.getSetting('settings.plugin-asl.userBrowserIcon');
        },
    },
    created() {
        this.listen(this.$state, 'plugin-asl.userbrowser.close', () => {
            this.active = false;
        });
    },
    methods: {
        toggleUserBrowser() {
            kiwi.showInSidebar(this.active ? null : UserBrowser);
            this.active = true;
        },
    },
};
</script>

<style>
.kiwi-header-user-browser-span {
    font-size: 1.28em;
}
</style>
